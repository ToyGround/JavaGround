package sec03.exam02;

public class IntegerExample {
    public static void main(String[] args) {
        int var1 = 0b1011; // 2
        int var2 = 0206; // 8
        int var3 = 206; // 10
        int var4 = 0xB3; // 16

        System.out.println("var 1 : " + var1);
        System.out.println("var 2 : " + var2);
        System.out.println("var 3 : " + var3);
        System.out.println("var 4 : " + var4);
    }
}
